# 1. Объявление класса
# Класс создается с помощью ключевого слова class, а его название записывается в стиле CamelCase.
# class Person:
#     # Описание чертежа будущего объекта
#     pass
# 2. Работа с атрибутами класса
# Даже если класс пустой, он сам по себе является объектом и имеет встроенные («магические») свойства.
# # Получение имени класса через магическое свойство __name__
# print(Person.__name__)  # Выведет: 'Person' [2]
#
# # Просмотр всех доступных атрибутов и методов класса
# print(dir(Person)) [2]
# 3. Создание экземпляров (объектов) класса
# Чтобы создать объект, нужно «вызвать» класс как функцию. Объекты, созданные на основе одного класса, имеют одинаковый набор характеристик, но разные состояния.
# # Создание экземпляра класса Person
# p = Person() [2]
#
# # Проверка типа объекта
# print(type(p))  # Возвращает сам класс как объект [2]
# 4. Динамическое создание объекта и сравнение
# Вы можете создать новый экземпляр того же типа, используя функцию type() от уже существующего объекта.
# # Создание нового экземпляра того же типа, что и объект p
# new_person = type(p)() [2]
#
# # Сравнение идентификаторов (ID) двух разных объектов
# print(id(p))
# print(id(new_person))
#
# # Это разные объекты в памяти, несмотря на то что они одного класса [4]
# Ключевые моменты из источников:
# • Объект — это выделенная оперативная память для хранения значений и связанных с ними операций.
# • Класс — это «чертеж» или описание, по которому Python создает объекты в памяти.
# • Доступ к свойствам и методам объекта осуществляется через точку (dot notation).
# • В Python абсолютно всё (включая сами классы, функции и модули) является объектами.
