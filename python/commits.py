#    - docs - изменение в документации
#    - style - исправление опечаток и форматирования кода
#    - refactor - улучшение структуры и читаемости кода без изменения смысла кода
#    - test - добавление или изменение тестов
#    - build - изменение того, как проходит сборка программы
#    - ci - изменение в работе CI/CD
#    - perf - улучшение производительности приложения
#    - chore - прочие изменения (обновление настроек, задач, etc)
