"""Выбор понятных имен в Python.

Конспект.
"""

# Конспект главы **«Выбор понятных имен»**:
#
# ---
#
# ### **Основная идея**
# Выбор хороших имен для переменных, функций, классов и других сущностей — критически важный аспект написания читаемого, поддерживаемого и понятного кода. Это субъективная, но ответственная задача программиста.
#
# ---
#
# ### **Ключевые принципы**
#
# 1. **Имена должны быть содержательными, но не избыточными**
#    - Избегайте слишком коротких (`a`, `b`, `g`) и слишком общих (`data`, `var`, `temp`) имен.
#    - Длинные имена предпочтительнее коротких, если они улучшают ясность (особенно для глобальных сущностей).
#    - Имя должно отражать назначение: `emailAddress` лучше, чем `email`.
#
# 2. **Следуйте соглашениям PEP 8 по стилю именования**
#    - **Классы**: `PascalCase` (например, `MyClass`).
#    - **Функции, методы, переменные**: `snake_case` (например, `calculate_total`).
#    - **Константы**: `UPPER_SNAKE_CASE` (например, `MAX_SIZE`).
#    - **Приватные атрибуты**: начинаются с `_` (например, `_internal_var`).
#    - **Аргументы методов**: `self` для экземпляров, `cls` для классов.
#
#       Важнейший фактор удобочитаемости — не выбор схемы, а последовательность в применении этой схемы.
#
# 3. **Избегайте вредных практик**
#    - **Не используйте встроенные имена Python** (`list`, `str`, `open`, `id` и др.) как имена своих переменных — это приводит к ошибкам.
#    - **Не применяйте венгерскую нотацию** (префиксы типа `strName`, `iCount`) — она устарела.
#    - **Не используйте шутки, каламбуры и культурные отсылки** (`gooseDownload()`) — они непонятны другим разработчикам, особенно не носителям языка.
#
# 4. **Делайте имена пригодными для поиска**
#    - Уникальные и конкретные имена легче находить через поиск (Ctrl+F) и реже дают ложные совпадения.
#
# 5. **Избегайте числовых суффиксов без смысла**
#    - Имена вроде `payment1`, `payment2` — признак плохой структуры. Лучше использовать коллекции (`payments = [...]`) или параметризованные функции (`make_payment(priority=1)`).
#
# 6. **Учитывайте единицы измерения**
#    - Если значение имеет размерность, включайте её в имя: `weight_kg`, `timeout_sec`. Это предотвращает критические ошибки (как с Mars Climate Orbiter).
#
# 7. **Используйте префиксы `is_`, `has_` для булевых значений**
#    - Пример: `is_ready`, `has_permission` — делает код читаемым как естественный язык.
#
# ---
#
# ### **Исключения**
# - Однобуквенные имена допустимы в узких контекстах: `i`, `j`, `k` — для индексов циклов; `x`, `y` — для координат.
#
#    Хотя допустим, w и h для ширины (width) и высоты (height) или n для числа
#    (number) могут соблазнить вас краткостью, другим это может быть не очевидно.
#
# ---
#
# ### **Заключение**
# Хорошие имена — основа качественного кода. Они делают его понятным не только для других, но и для вас самих в будущем. Следование рекомендациям (в первую очередь PEP 8) и здравый смысл помогут писать код, который легко читать, поддерживать и развивать.
#
# Старайтесь использовать короткие фразы, с которыми ваш код
# читается как обычный текст. Например, имя number_of_trials читается лучше,
# чем number_trials.

