# 4. Как установить необходимые пакеты внутрь виртуального окружения для conda/venv?
#
# Для venv: есть несколько способов:
# 1) Активация нужного окружения и установка нужных зависимостей с помощью команды pip install "имя зависимости"
# 2) Создаем файл requirements.txt записываем туда все необходимые зависимости и с помощью команды pip install -r requirements.txt устанавливаем их
#
# Для conda:
# 1) Активация нужного окружения и установка нужных зависимостей с помощью команды conda install "имя зависимости"
# 2) Создаем файл environment.yml записываем туда все необходимые зависимости и с помощью команды conda env update -f environment.yml устанавливаем их либо обновляем.
#
