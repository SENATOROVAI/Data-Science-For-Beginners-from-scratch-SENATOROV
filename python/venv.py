# 1. Что делает команда python -m venv venv?
#
# Данная команда создает виртуальное окружения под названием venv.
# Новое виртуальное окружение означает среду в которой происходит работа только с теми библиотеками которые будут установленны в данную
# среду.
#
# 1.1 Что делает каждая команда в списке ниже?
#
# pip list - показывает установленные зависимости
# pip freeze > requirements.txt - создает файл с установленными зависимостями
# pip install -r requirements.txt - устанавливает зависимости указанные в requirements.txt
#
# 2. Что делает каждая команда в списке ниже?
#
# conda env list - показывает список всех окружающих сред
# conda create -n env_name python=3.5 - создаем окружающую среду (окружение) в conda с названием env_name которая будет работать с python версии 3.5
# conda env update -n env_name -f file.yml - обновление окружения с помощью yml файла
# source activate env_name - команда активации виртуального окружение на операционных системах Linux/macOS
# source deactivate - деактивация использующего окружения на операционных системах Linux/macOS
