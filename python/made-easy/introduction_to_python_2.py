# #### Приложение Jupyter Notebook
# - Это редактор расширяет консольный подход к интерактивным вычислениям в качественно новом направлении и сконструирован как веб-приложение, подходящее для записи всего вычислительного процесса: разработки, документирования и выполнения кода, а также передачи результатов. Jupyter Notebook объединяет в себе два компонента:
#     - веб-приложение - инструмент на основе браузера, предназначенный для интерактивного создания документов, сочетающий в себе пояснительный текст, математические вычисления и мультимедийный вывод результатов;
#     - документы Notebook, содержащие все видимое в веб-браузере, включая входные и выходные данные вычислений, пояснительный текст, математические данные, изображения и мультимедийные представления объектов.
#
# #### Основные возможности веб-приложения
# - Jupyter Notebook позволяет работать с кодом в редакторе, встроенном в окно браузера, с автоматическим выделением синтаксиса цветом, отступами, заполнением табуляцией и анализом.
#
# #### Документы Notebook
# - Документы Notebook содержат входные и выходные данные интерактивного сеанса, а также дополнительный текст, который добавлен к коду, но не предназначен для выполнения. Эти документы являются внутренними файлами JSON и сохраняются с расширением ipynb. Поскольку JSON - это неформатированный текст, эти файлы также можно включать в контроль версий и делиться ими с коллегами. Документы Notebook можно экспортировать в некоторые статические форматы, например, НТМL (для публикации в блоrах), Restructured Text, LaTeX, PDF. Кроме того, любой документ Notebook формата ipynb, доступный по публичному URL-aдpecy, можно опубликовать через Jupyter Notebook Viewer (nbviewer).
