# ### Интерпретаторы Python
# - интерпретатор - это разновидность компьютерной программы, которая непосредственно выполняет инструкции, написанные на каком-либо языке программирования или языке сценариев. Под непосредственным выполнением подразумевается, что предварительная компиляция кода в программу на машинном языке не требуется. Давайте кратко рассмотрим 7 самых популярных интерпретаторов Python.
#
# #### IPython
# - IPython - это альтернативный интерпретатор Python. Это интерактивная оболочка, которая используется для вычислений на Python. В ней гораздо больше полезных функций по сравнению с интерпретатором Python, используемым по умолчанию. В комплекте с IPython идет редактор Jupyter Notebook - один из наиболее часто используемых начинающими программистами инструментов.
#
# #### CPython
# - Это стандартная и наиболее широко используемая реализация языка программирования Python. Написанный на С и Python, интерпретатор CPython содержит много внешних функций С и связан с другими языками программирования. CPython можно в какой-то степени считать компилятором, поскольку перед интерпретацией он преобразует код Python в байт-код. В нем используется GIL (глобальная блокировка интерпретатора), которая ограничивает возможности интерпретатора, поскольку она отключает параллельные потоки Python в рамках одного процесса.
#
# #### lronPython
# - IronPython - это реализация языка программирования Python для платформы .NET, в которой используются библиотеки Python и .NET. Этот интерпретатор может предоставлять код Python другим языкам, относящимся к платформе .NET.
#
# #### Jython
# Ранее известная как JPython, реализация Jython работает на платформе Java. Это сочетание Java и Python преобразует ;
# код Python в байт-код Java и, следовательно, позволяет запускать код Python на любой машине, на которой установлена JVM. Jython обеспечивает поддержку как статической, так и динамической компиляции. Важной особенностью этого интерпретатора Python является то, что он позволяет импортировать, а также использовать любые классы Java так, словно это модуль Python.
#
# #### РуРу
# - РуРу - это быстрая и совместимая альтернативная реализация языка программирования Python. Этот интерпретатор Python реализован в RPython, который представляет собой ограниченное статически типизированное подмножество языка программирования Python.
#
# #### Python Net
# - PythonNet позволяет выполнить почти бесшовную интеграцию Python с .NET CLR. Подход этого интерпретатора можно считать противоположным тому, которому следует lronPython. При использовании вместе с Mono, PythonNet позволяет встроенным в операционные системы установкам Python (кроме
# Windows) работать в среде .NET. PythonNet можно использовать совместно с IronPython без каких-либо проблем
#
# #### Stackless Python
# - CPython и другие популярные интерпретаторы Python для управления стеком используют язык С. Но к интерпретатору Stackless Python это не относится. Хотя в Stackless Python используется стек С, между вызовами функций он очищается. Следовательно, интерпретатор Python в работе со стеком не зависит от вызова С. Как и CPython, Stackless Python написан с использованием С и Python. Помимо поддержки потоков, у Stackless Python есть поддержка каналов связи, сопрограмм, предварительно скомпилированных двоичных файлов, циклического планирования, сериализации задач и тасклетов. Возможно, самая важная особенность Stackless Python - это наличие микропотоков. Эта возможность помогает избежать значительной части накладных: расходов, связанных с обычными потоками операционной системы.
#
# #### Объектная ориенти рованность
# - Python поддерживает процедурно-ориентированное программирование, а также объектно-ориентированное программирование. В процедурно-ориентированных: языках программа состоит из процедур или функций, в которых заключены многократно используемые части программы. В объектно-ориентированных языках программа построена на объектах, которые включают в себе данные и функциональность. В Python очень мощный, но упрощенный стиль объектно-ориентированного программирования, особенно по сравнению с такими крупными языками, как С++ или Java.
