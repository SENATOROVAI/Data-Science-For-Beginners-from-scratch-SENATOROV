# ### Компьютерный алгоритм
# - Чтобы заставить компьютер сделать что-либо нужно написать инструкции, затем компьютер запускает программу и выполняет её шаг за шагом чтобы сделать задачу. Данные инструкции называются алгоритмом.
# #### Примеры алгоритмов
# - Напишем алгоритм приготовления чая. Ингредиенты: вода, сахар, молоко и чайные листья на две чашки чая.
# - Шаг 1 . Начало.
# - Шаг 2: Возьмите ковшик и налейте в него один стакан воды.
# - Шаг 3: Зажгите конфорку и поставьте на нее ковшик.
# - Шаг 4: Доведите воду до кипения.
# - Шаг 5: Добавьте в кипящую воду две столовые ложки сахара и две столовые ложки чайных листьев.
# - Шаг 6: Добавьте в кипящую смесь одну чашку молока.
# - Шаг 7: Варите содержимое в течение одной минуты.
# - Шаг 8: Выключите конфорку.
# - Шаг 9: Процедите чай через сито.
# - Шаг 1О: Разлейте чай по чашкам и подавайте.
# - Шаг 11 : Конец.
#
# #### Пример вычислител ьного алгоритма
# - Напишите алгоритм того, как сложить 2 + 2 и получить 4 в качестве результата.
# - Шаг 1 : Начало.
# - Шаг 2: Введите 2 + 2.
# - Шаг 3: Нажмите клавишу <Enter>.
# - Шаг 4: Конец.
# - В целом алгоритмы состоят из 3 видов операторов, которые могут присутствовать вразличных комбинациях:
# 1. Последовательные операторы.
# 2. Условные операторы.
# 3. Циклы или итерации.
#
# #### Последовательные операторы
# - Эти операторы выполняются один за другим, последовательно.
# - Приведенные ранее примеры, как заваривание чая, так и вычисление 2 + 2, являются примерами последовательных операторов. Рассмотрим еще пару примеров.
# - Пример. Как рассчитать возраст человека по году его рождения.
# - Шаг 1 : Начало.
# - Шаг 2: Возьмите год рождения человека и сохраните это число как УОВ.
# - Шаг 3: Возьмите текущий год и сохраните его как СУ.
# - Шаг 4: Вычтите УОВ из СУ и сохраните как Age.
# - Шаг 5: Выведите значение Age.
# - Шаг 6: Конец.
#
# #### Условные операторы
# - В таких программах некоторая часть программы выполняется в зависимости от определенного условия. Если условие верное, компьютер выполняет одну часть программы, а если условие неверное, то выполняется другая часть программы.
# - Пример. Напишите алгоритм, который проверяет, заболел ли человек. Считаем, что человек заболел, если температура его тела выше 37 °С.
# - Шаг 1 : Начало.
# - Шаг 2: Измерьте температуру и сохраните ее в ТЕМР.
# - Шаг 3: Проверьте значение ТЕМР. Если оно превышает 37 градусов, переходите к шагу 4, в противном случае переходите к шагу 5.
# - Шаг 4: Выведите «Пациент болеет» и переходите к шагу 6.
# - Шаг 5: Выведите «Пациент здоров».
# - Шаг 6: Конец.
#
# #### Цикл или итерации
# - В некоторых программах определенный набор действий нужно выполнять снова и снова в зависимости от некоторого условия. Эти повторяющиеся действия называются итерациями. Итерация выполняется с использованием одного или нескольких операторов цикла, поэтому программы такого типа называются циклическими или повторяющимися.
# - Пример. Вывести на экран все числа от 1 до 1О.
# - Шаг 1 : Начало.
# - Шаг 2: Сохраните значение 1 в i.
# - Шаг 3: Проверьте, выполняется ли условие i <= 1О. Если да, то переходите к шагу 4. Если нет,переходите к шагу 7.
# - Шаг 4: Выведите значение i на экран.
# - Шаг 5: Увеличьте значение i на 1 .
# - Шаг 6: Переходите к шагу 3.
# - Шаг 7: Конец.
# - В этом примере шаги с 3-ro по 5-й повторяются до тех пор, пока условие шага 3 выполняется. Как только условие станет неверным, цикл завершится.
#
