# ### Что такое язык программирования?
# - Если мы хотим дать компьютеру какие-то указания, нужен язык программирования.Разных языков программирования существует очень много. Как и у разговорных языков, у всех языков программирования есть свои правила (синтаксис) и значения (семантика).
# - У всех языков программирования есть свои сильные и слабые стороны. Некоторые из них идеально подходят для создания веб-сайтов, другие - для мобильных приложений, а третьи предназначены для выполнения сложных математических расчетов.
# - Независимо от того, какой язык программирования вы выбрали, компьютер все равно его не понимает. Компьютер понимает совершенно другой язык, который называется машинным языком. Но нам бьmо бы сложно писать программ ы напрямую на машинном языке, потому что он очень сложен и состоит в основном из последовательностей чисел. Именно поэтому приведенные выше языки программирования называются языками высокого уровня, т. к. они более похожи на естественные человеческие языки.

# ### Что такое исходный код?
# - Исходный код - это то, что пишуг программисты на всех языках программирования.Это те самые указания, которые мы даем компьютеру, написанные неформатированным текстом.

# ### Как запустить исходны й код?
# Существуют три основных способа перевода исходного кода в машинный код:
# - компиляция;
# - интерпретация;
# - сочетание этих двух способов.
#
# #### Компилятор
# - Компилятор - это программа, преобразующая исходный код в машинный.
#
# #### Интерпретатор
# - интерпретатор - это компьютерная программа, которая непосредственно выполняет инструкции, написанные на языке программирования, без предварительной компиляции в программу на машинном языке.

# ### Сравнение компилятора и интерпретатора
# Основные различия между компиляторами и интерпретаторами:
# - И компиляторы, и интерпретаторы преобразуют исходный код (текстовые файлы) в машинный язык. Основное их отличие состоит в том, что компилятор генерирует автономную программу, написанную на машинном коде, а система интерпретатора выполняет действия, описанные программой высокого уровня.
# - После компиляции программы ее исходный код для запуска больше не требуется. У интерпретируемых программ исходный код требуется для запуска программы каждый раз.
# - Обычно интерпретируемые программы работают медленнее, чем компилируемые.
#

# ### Что такое интегрированная среда разработки (IDE)?
#
# - Для решения специфичных задач всегда существует специальное программное обеспечение. Например, когда нужно работать с данными, таблицами и расчетами, мы используем программы для работы с электронными таблицами (например, Microsoft Ехсе), а если нужна презентация, используем PowerPoint. Для создания или открытия текстовых документов используются текстовые редакторы. Нетрудно предположить, что для написания компьютерных программ существуют интегрированные среды разработки или IDE (lntegrated Development Environrnent). В этих программах есть специальные инструменты, необходимые для написания, отладки и ком.пиляции кода.
# - Поскольку мы изучаем Python, мы будем использовать IDE, поддерживающие язык Python. Дефицита IDE для Python не наблюдается - можно использовать любую. Но я рекомендую начать со стандартного программного обеспечения, т. к. оно проще в установке и использовании. Скорее всего, вы так и будете использовать эти программы дальше, поскольку они весьма эффективны.

# ### Spyder IDE
# - Spyder IDE (Scientific Python Development Environment) - это бесплатная научная среда разработки на Python, которая входит в состав дистрибутива Anaconda (пакетное программное обеспечение для Python). В этой IDE есть функции редактирования, проверки, отладки и интерактивного тестирования.

