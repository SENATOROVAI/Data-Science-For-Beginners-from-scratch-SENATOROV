# ## Настоящее и будущее Data Science
#
# Data Science идет по нескольким направлениям:
#
# - Искусственный интеллект (ИИ) - это сфера, в которой основное внимание уделяется созданию интеллектуальных машин, способных работать и принимать
# решения как человек.
#
# ![image.png](attachment:image.png)
#
# - Машинное обучение - это инструмент для извлечения знаний из данных. В машинном обучении модели могут обучаться на данных самостоятельно или
# поэтапно: обучение с учителем, т. е. на данных, подготовленных человеком, или обучение без учителя, в котором работа ведется над хаотичными и неорганизованными данными.
# - Глубокое обучение - это создание многослойных нейронных сетей в областях, где требуется более продвинутый или быстрый анализ, а традиционное машинное обучение не справляется. Под глубиной понимается наличие более одного скрытого слоя нейронов в сети, которые проводят математические вычисления.
# - Большие данные - это работа с огромными объемами часто неструктурированных данных. Специфика этой сферы - инструменты и системы, способные
# выдерживать высокие нагрузки.
#
#
#

# ## Чем занимается специалист по Data Science?
#
# «Специалист по Data Science - это человек, который разбирается в статистике лучше любого программиста и программирует лучше любого статистика» (Джош Уиллс).
#
# Специалист по Data Science анализирует бизнес-данные, извлекая из них важную информацию.Cпециалист по анализу данных решает бизнесзадачи
# с помощью следующих шагов:
#
# - задает правильные вопросы, чтобы понять проблему;
# - собирает данные из нескольких источников - корпоративные данные, общедоступные данные и т. д.;
# - обрабатывает сырые данные и преобразует их в формат, подходящий для анализа;
# - загружает данные в аналитическую систему - алгоритм машинного обучения или статистическая модель;
# - подготавливает результаты и идеи, которые можно изложить заинтересованным сторонам.
#
# Всё это позволяет решить немало проблем, некоторые из них приведены далее:
#
# -  обнаружение мошенничества и выявление аномалий, например изменений схемы снятия или расходования средств с кредитной карты клиента;
# - целевой и персонализированный маркетинг - персональные рассылки по электронной почте, системы рекомендаций на сайтах магазинов;
# - метрические прогнозы - показатели эффективности, качества рекламных кампаний и других мероприятий;
# - оценка принятия решений - обработка больших объемов данных и помощь в принятии решения, например о выдаче кредита на основе кредитных оценок;
# - прогнозирующее моделирование - прогнозирование столкновения метеорита с землей на основе астрономических данных.
# Это лишь малая часть тем, которыми занимается специалист по данным.

