def number_snake() -> None:
    """number_snake."""
    n_snake: int = int(input())
    m_snake: int = int(input())
    max_numb_s: int = m_snake * n_snake
    width_s: int = 0
    temp_s: int = max_numb_s
    while temp_s:
        width_s += 1
        temp_s //= 10
    if max_numb_s == 0:
        width_s = 1
    for row_s in range(n_snake):
        for col_s in range(m_snake):
            numb_snake: int = (
                (row_s * m_snake + col_s + 1)
                if row_s % 2 == 0
                else (row_s * m_snake + m_snake - col_s)
            )
            print(f"{numb_snake:>{width_s}}", end=" ")
        print()


