def greatest_common_divisor_2() -> None:
    """greatest_common_divisor_2."""
    numbers_div: int = int(input())
    current_gcd: int = int(input())
    for _ in range(numbers_div - 1):
        numb_div: int = int(input())
        a_div: int = current_gcd
        b_div: int = numb_div
        while b_div:
            a_div, b_div = b_div, a_div % b_div
        current_gcd = a_div
        if current_gcd == 1:
            break
    print(current_gcd)


