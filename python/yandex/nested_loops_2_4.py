def number_square() -> None:
    """number_square."""
    size_squ: int = int(input())
    cell_width: int = len(str((size_squ + 1) // 2))
    for i in range(size_squ):
        for j_squ in range(size_squ):
            print(
                f"{min(i + 1, j_squ + 1, size_squ - i, size_squ - j_squ):>{cell_width}}",
                end=" ",
            )
        print()


if __name__ == "__main__":
    number_square()


