def mathematical_benefit() -> None:
    """mathematical_benefit."""
    number_math: int = int(input())
    max_sum_math: int = -1
    best_base: int = 2
    for base in range(2, 11):
        n_math: int = number_math
        current_sum: int = 0
        while n_math:
            current_sum += n_math % base
            n_math = n_math // base
        if current_sum > max_sum_math:
            max_sum_math = current_sum
            best_base = base
        elif current_sum == max_sum_math and base < best_base:
            best_base = base
    print(best_base)


if __name__ == "__main__":
    mathematical_benefit()
