def number_rectangle() -> None:
    """number_rectangle."""
    n_rec: int = int(input())
    m_rec: int = int(input())
    max_number: int = m_rec * n_rec
    width: int = 0
    temp: int = max_number
    while temp:
        width += 1
        temp //= 10
    if max_number == 0:
        width = 1
    for row in range(n_rec):
        for col in range(m_rec):
            number_rec: int = row * m_rec + col + 1
            print(f"{number_rec:>{width}}", end=" ")
        print()


