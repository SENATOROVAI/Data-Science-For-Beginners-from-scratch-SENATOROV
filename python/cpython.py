# 1. Что такое CPython и чем он отличается от Python?
#
# Ответ:
#
# CPython — стандартная реализация Python на языке C. Это та версия, которую обычно скачивают с python.org.
#
# Разница:
# Python — язык программирования (спецификация)
# CPython — реализация этого языка на C
#
#
# 2. Сколько существует реализаций Python, и какая из них самая популярная?
