#
#
# 9. Где можно найти исходный код CPython дайте ссылку на репозиторий гитхаб
#
# Ответ:
#
# https://github.com/python/cpython
#
#
# 10. (опционально) Как работает интерпретатор CPython при выполнении кода?
#
# Ответ:
#
# Парсинг — исходный код разбирается в AST (абстрактное синтаксическое дерево).
# Компиляция в байт-код — AST компилируется в байт-код (.pyc), который хранится в папке __pycache__.
#
# Виртуальная машина (VM) — байт-код выполняется стековой виртуальной машиной:
# Читает инструкции байт-кода
# Выполняет их через C-функции
# Управляет стеком вызовов, объектами в памяти, GIL
# Выполнение — каждая инструкция байт-кода вызывает соответствующую C-функцию, которая выполняет операцию.
#
#
# 11. Какая команда используется для запуска файла с помощью CPython?
#
# Ответ:
#
# python filename
#
# 12. Можно ли запускать текстовые файлы через интерпретатор Python? Почему?
#
# Ответ:
#
# Да, можно
# Интерпретатору без разницы какой формат файла, главное, чтобы был валидный пайтон код
#
# 13. Как указать путь к интерпретатору и файлу для выполнения кода?
#
# Ответ:
#
# Через Абсолютный или относительный путь, или если есть алиас в переменных средах, то через него
#
# 14. Чем PyPy отличается от CPython?
#
# Ответ:
#
# PyPy — альтернативная реализация Python с JIT-компиляцией, быстрее CPython для многих задач, но с ограничениями по совместимости с C-расширениями
#
# 15. Почему PyPy не может использоваться для всех проектов на Python?
#
# Ответ:
#
# Не совместим со всеми проектами на Python
#
#
# 16. Где можно скачать PyPy?
#
# Ответ:
#
# https://pypy.org/download.html
#
#
# 17. Как установить PyPy после скачивания?
#
# Ответ:
#
# Распаковать в нужную папку и прописать пути в переменных средах
#
# 18. Как запустить файл с помощью PyPy?
#
# Ответ:
#
# pypy filename
# Или абсолютный путь до интерпретатора:
# ~/pypy/pypy.exe filename
#
# 19. Почему PyPy выполняет код быстрее, чем CPython?
#
# Ответ:
#
# PyPy быстрее за счёт JIT-компиляции часто используемого кода в машинный код, что устраняет интерпретацию байт-кода.

